void main() {
  // Jumlah uang yang ditabung setiap hari
  int uangPerHari = 5000;
  
  // Jumlah hari
  int jumlahHari = 30;
  
  // Menghitung total uang di celengan
  int totalUang = uangPerHari * jumlahHari;
  
